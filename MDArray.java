public class MDArray{
    public static void main(String[] args){
        int Arr[][]={{11,22,33},{44,55,66}};
        for(int i=0;i<2;i++){
            for(int j=0;j<3;j++){
                System.out.print(Arr[i][j]+" ");
            }
            System.out.println();
        }
    }
}
